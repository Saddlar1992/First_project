void main() {

  String name = "Bakhtier";

  print(name.toLowerCase()); // все буквы с нижнего регистра
  print(name.toUpperCase()); // все буквы с верхнего регистра
  print(name.isEmpty); // строка пустая = Фолс
  print(name.isNotEmpty); // строка не пустая = Тру
  print(name.length); // количестов символов в строке
  print(name.contains('B')); // поиск внтури строки
  print(name.substring(2)); // возвращает строку с определенного индекса


}